static void AES_cbc_encrypt(aes_context *ctx, u8 *src, u8 *dst, int size)
{
  u8 iv[16];
  
  memset(iv, 0, 16);
  aes_crypt_cbc(ctx, AES_ENCRYPT, size, iv, src, dst);
}

static void AES_cbc_decrypt(aes_context *ctx, u8 *src, u8 *dst, int size)
{
  u8 iv[16];
  
  memset(iv, 0, 16);
  aes_crypt_cbc(ctx, AES_DECRYPT, size, iv, src, dst);
}

/* AES-CMAC Generation Function */

static void leftshift_onebit(u8 *input, u8 *output)
{
  u8 overflow = 0;
    
  for (int i=15; i>=0; i--)
  {
    output[i] = input[i] << 1;
    output[i] |= overflow;
    overflow = (input[i] & 0x80)?1:0;
  }
}

static void xor_128(u8 *a, u8 *b, u8 *out)
{
  for (int i=0;i<16; i++)
    out[i] = a[i] ^ b[i];
}

static void generate_subkey(aes_context *ctx, u8 *K1, u8 *K2)
{
  u8 L[16];
  u8 Z[16];
  u8 tmp[16];
    
  memset(Z, 0, sizeof(Z));
  aes_crypt_ecb(ctx, AES_ENCRYPT, Z, L);
    
  if ( (L[0] & 0x80) == 0 ) /* If MSB(L) = 0, then K1 = L << 1 */
  {
  	leftshift_onebit(L,K1);
  } else {    /* Else K1 = ( L << 1 ) (+) Rb */
    leftshift_onebit(L,tmp);
    xor_128(tmp,const_Rb,K1);
  }

  if ( (K1[0] & 0x80) == 0 ) 
  {
    leftshift_onebit(K1,K2);
  } else {
    leftshift_onebit(K1,tmp);
    xor_128(tmp,const_Rb,K2);
  }
}

static void padding(u8 *lastb, u8 *pad, int length)
{
  /* original last block */
  for (int j=0; j<16; j++)
  {
	if ( j < length ) 
	{
      pad[j] = lastb[j];
    } else if ( j == length ) {
      pad[j] = 0x80;
    } else {
      pad[j] = 0x00;
    }
  }
}

static void AES_CMAC(aes_context *ctx, u8 *input, int length, u8 *mac)
{
  u8 X[16], Y[16], M_last[16], padded[16];
  u8 K1[16], K2[16];
  int n, i, flag;
  generate_subkey(ctx,K1,K2);

  n = (length+15) / 16;       /* n is number of rounds */

  if ( n == 0 ) 
  {
    n = 1;
    flag = 0;
  } else {
	if ( (length%16) == 0 ) { /* last block is a complete block */
      flag = 1;
    } else { /* last block is not complete block */
      flag = 0;
    }
  }

  if ( flag ) { /* last block is complete block */
    xor_128(&input[16*(n-1)],K1,M_last);
  } else {
    padding(&input[16*(n-1)],padded,length%16);
    xor_128(padded,K2,M_last);
  }

  memset(X, 0, sizeof(X));
  for ( i=0; i<n-1; i++ ) 
  {
    xor_128(X,&input[16*i],Y); /* Y := Mi (+) X  */
    aes_crypt_ecb(ctx, AES_ENCRYPT, Y, X); /* X := AES-128(KEY, Y); */
  }

  xor_128(X,M_last,Y);
  aes_crypt_ecb(ctx, AES_ENCRYPT, Y, X);
  memcpy(mac, X, sizeof(X));
}

